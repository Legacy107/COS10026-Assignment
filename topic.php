                        <h2>What is MP3?</h2>
                        <p>MP3 is a coding format for digital audio.<br/><br/>The MP3 format is named after the MPEG standards, MPEG-1 audio layer 3 and MPEG-2 Audio Layer 3, this is abbreviated to MP3. Additional to the two stated before, there is another unofficial MP3 standard called MPEG 2.5.<br/><br/>The key feature of the MP3 format is the way it goes about compressing digital audio this process enables it to be used in situations with smaller storage space requirements. The type of compression MP3 uses is called lossy data compression.<br/><br/>Lossy data-compression works by removing any small bits of data that arenâ€™t necessary for the audio to be stored, with a secondary goal of this being done without causing any distortions of artifacts in the audio.<br/><br/>This kind of compression does make the original form of the data irretrievable, however, the compressed data can be stored at a much smaller file size than the original information.<br/><br/>Depending on the purpose of the audio, differing amounts of data loss and artifacting can be tolerated. For example, a telephone call can lose more information than a compact disc as the purpose of a telephone call is less dependant on the quality of the audio.<br/><br/>This compression technology has had a long history, and has been involved in many scandals, including plagiarism of popular music by parties with no legal rights to the music.<br/><br/>And while plagiarism hasn't really affected the popularity of the format, newer audio formats and a wider range of technologies such as smartphones and watches, have started to replace older technologies such as the Winamp player, which was a behemoth of the MP3 format in the days of broadband.</p>
